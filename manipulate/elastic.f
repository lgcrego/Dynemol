subroutine selecting( sys, instance, message )
!=============================================
implicit none 
type(universe)  , intent(inout) :: sys
integer         , intent(in)    :: instance
character(len=*), intent(in)    :: message

! local variables ...
integer                        :: i
integer          , allocatable :: nr(:) , indx(:)
character(len=1)               :: choice
character(len=3) , allocatable :: residue(:)
character(len=80)              :: line
logical          , allocatable :: mask(:)

! reset varible ...
select case(instance)
   case(1) 
   sys% atom(:)% translate = .false.
   case(2) 
   sys% atom(:)% DWFF = .false.
end select

CALL systemQQ( "clear" )

write(*,'(/a)') bold // orange // message // reset

write(*,'(a)') bold // cyan // ' (1) = tuning already done' // reset
write(*,'(a)') bold // cyan // ' (2) = residue number ' // reset
write(*,'(a)') bold // cyan // ' (3) = residue name ' // reset
write(*,'(a)') bold // cyan // ' (4) = atom index ' // reset

write(*,'(/a)', advance='no') bold // yellow // '>>>   ' // reset
read (*,'(a)') choice

associate( atom => sys% atom)

    allocate( mask(size(atom)) )
    
    select case( choice )
    
        case( '1' )
            ! do nothing, proceed ...
    
        case( '2' )
            write(*,'(/a)', advance='no') green // &
            "enter the residue numbers to be changed: separated by spaces, or in the format 'first:last' (press ENTER to send) : " // reset
            read (*,'(a)') line
    
            nr = parse_this(line)
            do i = 1 , size(nr)
    
                mask = (atom% nresid == nr(i))
    
                select case (instance)
                case (1)
                    where(mask) atom(:)% translate = .true.
                case (2)
                    where(mask) atom(:)% DWFF = .true.
                end select
            end do
    
        case( '3' )
            write(*,'(/a)', advance='no') green // &
            "enter the residue names to be changed, separated by spaces (press ENTER to send) : " // reset
            read (*,'(a)') line
    
            residue = split_line(line)
            do i = 1 , size(residue)
    
                mask = (atom% resid == residue(i))
    
                select case (instance)
                case (1)
                    where(mask) atom(:)% translate = .true.
                case (2)
                    where(mask) atom(:)% DWFF = .true.
                end select
            end do
    
        case( '4' )
            write(*,'(/a)') green // "enter the indices of the atoms to be changed," // reset
            write(*,'(a)', advance='no') green // "separated by spaces, or in the format 'first:last' (press ENTER to send) : " // reset
            read (*,'(a)') line
    
            indx = parse_this(line)
            print*, indx
            select case( instance )
                   case(1)
                       atom(indx)% translate = .true.
                   case(2)
                       atom(indx)% DWFF = .true.
            end select
    
    end select
end associate

deallocate(mask)

end subroutine selecting
