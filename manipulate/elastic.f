!====================================================
 subroutine Build_Configurations( sys, pes_scan_flag)
!====================================================
implicit none 
type(universe)            , intent(inout) :: sys
character(len=*), optional, intent(in)    :: pes_scan_flag

! local variables ...
integer                        :: i, n, n_steps, pes 
real*8                         :: versor(3)
real*8                         :: displacement, step
real*8           , allocatable :: config_erg(:)
logical                        :: pes_scan = .false.
character(len=96)              :: string
type(universe)   , allocatable :: trj(:)


if(present(pes_scan_flag) .and. (pes_scan_flag=="y")) pes_scan=.true.

call user_interface( sys, n_steps, step, versor , pes_scan)  

! generate configurations ...
allocate( trj(0:n_steps) )

if(pes_scan) then
    allocate( config_erg(0:n_steps) )
    OPEN(file='pes_erg.dat', status='unknown', action='write', newunit=pes)
end if

! bring system to the center
call bring_to_GCenter(sys%atom)

do n = 0, n_steps

    displacement = n * step  
    trj(n) = sys
    
    write(string,'(A55,F7.4)') "reaction path generated by DynEMol, this bond displacement = ", displacement
 
    trj(n)% System_Characteristics = string

    do concurrent ( i=1:sys%N_of_atoms , sys%atom(i)%translate ) 
            trj(n)%atom(i)%xyz = sys%atom(i)%xyz + versor * displacement
    end do
 
    if(pes_scan) then
        config_erg(n) = configuration_energy(trj(n)%atom)
        write(pes,'(2f10.4)') displacement, config_erg(n)
    end if

end do

call Save_PDB_Trajectories(trj) 

end subroutine Build_Configurations
